#five arithmetic operations

num1 = int(input('Enter First number: '))
num2 = int(input('Enter Second number '))
add = num1 + num2
dif = num1 - num2
mul = num1 * num2
div = num1 / num2
power = num1 ** num2

#printing the values

print('Sum is :',add)
print('Difference is :',dif)
print('Product is :',mul)
print('Division is :',div)
print('Exponent is :',power)
